package com.gmoa.bean;

public class LineChainCompare {
	private String lineId;
	private String enterTimes;
	private String exitTimes;
	private String enterDvalue;
	private String exitDvalue;
	private String startTime;
	public String getLineId() {
		return lineId;
	}
	public void setLineId(String lineId) {
		this.lineId = lineId;
	}
	public String getEnterTimes() {
		return enterTimes;
	}
	public void setEnterTimes(String enterTimes) {
		this.enterTimes = enterTimes;
	}
	public String getExitTimes() {
		return exitTimes;
	}
	public void setExitTimes(String exitTimes) {
		this.exitTimes = exitTimes;
	}
	public String getEnterDvalue() {
		return enterDvalue;
	}
	public void setEnterDvalue(String enterDvalue) {
		this.enterDvalue = enterDvalue;
	}
	public String getExitDvalue() {
		return exitDvalue;
	}
	public void setExitDvalue(String exitDvalue) {
		this.exitDvalue = exitDvalue;
	}
	public String getStartTime() {
		return startTime;
	}
	public void setStartTime(String startTime) {
		this.startTime = startTime;
	}
	
}
