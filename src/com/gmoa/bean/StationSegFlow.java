package com.gmoa.bean;

public class StationSegFlow {
	private String timeSeg;
	private String totalTimes;
	private String enterTimes;
	private String exitTimes;
	private String totalPkTimes;
	private String pkEnterTimes;
	private String pkExitTimes;
	
	public String getTimeSeg() {
		return timeSeg;
	}
	public void setTimeSeg(String timeSeg) {
		this.timeSeg = timeSeg;
	}
	public String getTotalTimes() {
		return totalTimes;
	}
	public void setTotalTimes(String totalTimes) {
		this.totalTimes = totalTimes;
	}
	public String getTotalPkTimes() {
		return totalPkTimes;
	}
	public void setTotalPkTimes(String totalPkTimes) {
		this.totalPkTimes = totalPkTimes;
	}
	public String getEnterTimes() {
		return enterTimes;
	}
	public void setEnterTimes(String enterTimes) {
		this.enterTimes = enterTimes;
	}
	public String getExitTimes() {
		return exitTimes;
	}
	public void setExitTimes(String exitTimes) {
		this.exitTimes = exitTimes;
	}
	public String getPkEnterTimes() {
		return pkEnterTimes;
	}
	public void setPkEnterTimes(String pkEnterTimes) {
		this.pkEnterTimes = pkEnterTimes;
	}
	public String getPkExitTimes() {
		return pkExitTimes;
	}
	public void setPkExitTimes(String pkExitTimes) {
		this.pkExitTimes = pkExitTimes;
	}
	
}
